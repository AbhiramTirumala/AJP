
public class Sum {

	public static void main(String[] args) {
		//Declaring and Initializing variables
		int i=1,sum=0;
		//Defining While loop with Condition
		while(i<=20)
		{
			//Using Arithemetic Operators for Calcualtion of Sum
			sum+=i;
			i++;
		}
		//Printing the Sum of 1 to 20 Numbers
		System.out.println("The Sum of Numbers from 1 to 20 is :"+sum);
	}

}
